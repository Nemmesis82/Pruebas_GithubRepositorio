package Pildoras.Informaticas;

public class Uso_Bucle_For {

	public static void main(String[] args) 
	{
		
		for (int i = 0; i < 10; i++)
		{
		
			System.out.println("Ruth" + i );
			
		}

		System.out.println("\n");
		
		for (int i = 10; i > 0; i--)
		{
		
			System.out.println("Ruth" + i );
			
		}
	}

}
